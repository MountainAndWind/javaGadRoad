package test.EXCEL;

public class Mobile {

    private String sqr;
    private String je;
    private String bxyf;
    private String count;

    public Mobile(String sqr, String je, String bxyf, String count) {
        this.sqr = sqr;
        this.je = je;
        this.bxyf = bxyf;
        this.count = count;
    }

    public String getSqr() {
        return sqr;
    }

    public String getJe() {
        return je;
    }

    public String getBxyf() {
        return bxyf;
    }

    public String getCount() {
        return count;
    }
}
